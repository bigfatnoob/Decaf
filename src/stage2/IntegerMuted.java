public class IntegerMuted {
	
	private Integer value;
	
	public IntegerMuted(Integer value) {
		this.value = value;
	}
	
	public IntegerMuted(){
		this.value = 0;
	}
	
	public Integer getValue() {
		return value;
	}

	public void setValue(Integer value) {
		this.value = value;
	}
	
}
